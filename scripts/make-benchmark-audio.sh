#!/usr/bin/env bash
# Генерирует воспроизводимый корпус русской речи для ASR-бенчмарка.
# Голос macOS `say` (Milena) → 16 кГц mono PCM WAV — формат, который ждёт whisper.cpp.
#
# Замечание о репрезентативности: синтетический голос чистый, без шума и пауз,
# поэтому корпус валиден для замера СКОРОСТИ/RTF (baseline latency), но не для
# оценки качества (WER). Для качества добавь реальные записи в ту же папку.
set -euo pipefail

VOICE="${VOICE:-Milena}"
OUT_DIR="${1:-$(cd "$(dirname "$0")/.." && pwd)/benchmarks/audio}"
mkdir -p "$OUT_DIR"

# id|текст. Длины подобраны от короткой реплики до длинного абзаца с цифрами,
# вопросами и техническими терминами — типичные для диктовки сценарии.
PHRASES=(
  "01-short|Привет, как дела? Давай созвонимся завтра в три часа дня."
  "02-medium|Сегодня нужно закончить отчёт по проекту, отправить его команде и запланировать встречу на следующей неделе. Не забудь проверить почту."
  "03-long|Локальная диктовка должна работать быстро и точно даже на длинных фразах. Я хочу продиктовать абзац из нескольких предложений, в котором есть запятые, точки и вопросительные знаки. Сколько времени это займёт? Посмотрим на задержку и качество распознавания."
  "04-terms|Открой проект Lyra Voice и запусти whisper. Нужно обработать пять файлов до пятнадцати ноль-ноль, а потом задеплоить на сервер."
)

echo "Генерация корпуса в: $OUT_DIR (голос: $VOICE)"
for item in "${PHRASES[@]}"; do
  id="${item%%|*}"
  text="${item#*|}"
  aiff="$OUT_DIR/$id.aiff"
  wav="$OUT_DIR/$id.wav"
  say -v "$VOICE" "$text" -o "$aiff"
  # LEI16@16000 = 16-bit signed PCM, 16 кГц; -c 1 = mono.
  afconvert "$aiff" "$wav" -f WAVE -d LEI16@16000 -c 1
  rm -f "$aiff"
  dur=$(afinfo "$wav" 2>/dev/null | awk -F'[:=]' '/estimated duration/ {gsub(/[^0-9.]/,"",$2); print $2}')
  printf "  ✓ %s  (%s c)\n" "$id.wav" "${dur:-?}"
done
echo "Готово: $(ls "$OUT_DIR"/*.wav | wc -l | tr -d ' ') файлов"
